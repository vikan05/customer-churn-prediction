""" FEATURE ENGINEERING (նոր հատկանիշների ստեղծում)"""

# 3.1 ServicesCount — քանի ծառայություն ունի հաճախորդը (0-ից 8)
service_cols = [col for col in ['PhoneService', 'OnlineSecurity', 'OnlineBackup',
                'DeviceProtection', 'TechSupport', 'StreamingTV', 'StreamingMovies']
                if col in df.columns]
for col in service_cols:
    df[col + '_bin'] = (df[col] == 'Yes').astype(int)

internet_bin = (df['InternetService'] != 'No').astype(int)
df['ServicesCount'] = df[[c + '_bin' for c in service_cols]].sum(axis=1) + internet_bin
df.drop([c + '_bin' for c in service_cols], axis=1, inplace=True)

# 3.2 IsNewCustomer — նոր հաճախորդ (tenure < 12 ամիս)
df['IsNewCustomer'] = (df['tenure'] < 12).astype(int)

# 3.3 AvgMonthlyPayment — TotalCharges / tenure (ամսական միջին)
# tenure=0 դեպքում կարող է division by zero, այդ պատճառով clip
df['AvgMonthlyPayment'] = df.apply(
    lambda row: row['TotalCharges'] / row['tenure'] if row['tenure'] > 0 else row['MonthlyCharges'],
    axis=1
)

# 3.4 HighSpender — MonthlyCharges > 70
df['HighSpender'] = (df['MonthlyCharges'] > 70).astype(int)

# 3.5 ChargesPerService — MonthlyCharges / ServicesCount
df['ChargesPerService'] = df.apply(
    lambda row: row['MonthlyCharges'] / row['ServicesCount'] if row['ServicesCount'] > 0 else row['MonthlyCharges'],
    axis=1
)

